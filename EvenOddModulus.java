public class EvenOddModulus {
    public static void main(String[] args) {
        // You can change this number to test different values
        int number = 14; 

        if (number % 2 == 0) {
            System.out.println(number + " is an even number.");
        } else {
            System.out.println(number + " is an odd number.");
        }
    }
}
